class Student:
  student_list = []
  def add_student(self,enroll_no,name,course):
    for student in self.student_list:
      if student["Enroll No"] == enroll_no:
        raise ValueError("Student Already Exists")
      if student["Name"] == name and student["Enroll No"] == enroll_no and student["course"] == course:
        raise ValueError("Course Already Registered")
    self.student_list.append({"Enroll No":enroll_no,"Name":name,"Course":course})
  def display_student(self):
    for i in self.student_list:
      print(i)
student1 = Student()
