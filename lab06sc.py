class Book:
  book_bank = []
  def add_book(self,book_id,title,author,num_copies):
    for book in self.book_bank:
      if book["Id"] == book_id:
        raise ValueError("ID Should Be Unique!!!")
        return
    if num_copies < 0:
        raise ValueError("Book Copies Cannot Be Negative")
        return
    self.book_bank.append({"Id":book_id,"Title":title,"Author":author,
                            "Number Of Copies":num_copies})
  def simplified_view(self):
    for i in self.book_bank:
      print(i)
